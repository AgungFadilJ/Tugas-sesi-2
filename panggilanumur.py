umurnya = int(input("masukan umur : "))

if umurnya <= 2 :
    print("dia adalah Bayi umur %i tahun"% umurnya )
elif umurnya <=5 :
    print("dia adalah Balita umur %i tahun "% umurnya )
elif umurnya <= 12 :
    print("dia adalah Anak-Anak umur %i tahun "% umurnya )
elif umurnya <= 17 :
    print("dia adalah Remaja umur %i tahun "% umurnya)
elif umurnya <= 30 :
    print("dia adalah Dewasa umur %i tahun "% umurnya )
else :
    print("dia adalah Orang Tua umur %i tahun "% umurnya )